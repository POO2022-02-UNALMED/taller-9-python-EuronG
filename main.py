#funciones
n1 = 0
n2 = 0
op = ""
def func_op(n1, op, n2):
    n1 = float(n1)
    n2 = float(n2)
    sol = 0

    if op == "+":
        sol = n1+n2
    elif op == '-':
        sol = n1-n2
    elif op == '*':
        sol = n1*n2
    elif op == '/':
        sol = n1/n2

    pantalla.delete(0, END)
    pantalla.insert(0, sol)

def num(n):
    pantalla.insert(END, n)


