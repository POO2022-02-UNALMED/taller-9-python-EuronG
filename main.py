def bmult(event):
    num("*")
def bdiv(event):
    num("/")

def bigual(event):
    nums = re.split(', |/|\-|\*|\+', pantalla.get())
    op = pantalla.get()[len(nums[0])]
    func_op(nums[0], op, nums[1])

